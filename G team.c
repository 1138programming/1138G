void driveBackward(int leftSpeed, int rightSpeed)
{
	motor[rightRearMotor] = -rightSpeed;
	motor[rightFrontMotor] = -rightSpeed;
	motor[leftFrontMotor] = -leftSpeed;
	motor[leftRearMotor] = -leftSpeed;
	//wait1Msec(time);
}
